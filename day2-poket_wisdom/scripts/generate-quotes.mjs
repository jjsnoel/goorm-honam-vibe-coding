function parseNobelFromMd() {
  const md = readFileSync(join(root, "poket-wisdom-quotes.md"), "utf8");
  const footnotes = {};
  const fnRegex = /^\[(\d+)\]:\s+(\S+)/gm;
  let m = fnRegex.exec(md);
  while (m) {
    footnotes[Number(m[1])] = m[2];
    m = fnRegex.exec(md);
  }

  function getPrize(url) {
    if (!url) return "";
    if (url.includes("/prizes/peace/")) return "노벨 평화상";
    if (url.includes("/prizes/literature/")) return "노벨 문학상";
    if (url.includes("/prizes/chemistry/")) return "노벨 화학상";
    if (url.includes("/prizes/economic")) return "노벨 경제학상";
    if (url.includes("caltech")) return "노벨 물리학상";
    if (url.includes("iadb") || url.includes("edge.org")) return "노벨 경제학상";
    return "";
  }

  const rows = [];
  for (const line of md.split("\n")) {
    if (!line.startsWith("|")) continue;
    const cells = line.split("|").slice(1, -1).map((c) => c.trim().replace(/^"+|"+$/g, ""));
    if (cells.length < 4 || !/^\d+$/.test(cells[0])) continue;
    const id = Number(cells[0]);
    const fnMatch = cells[3].match(/\[(\d+)\]/);
    const prize = getPrize(fnMatch ? footnotes[Number(fnMatch[1])] : "");
    const years = cells[3].replace(/\s*\([^)]*\[[^\]]+\]\)\s*$/, "").trim();
    const profession = PROFESSION_BY_ID[id] || "";
    rows.push({
      quote: cells[1],
      person: cells[2],
      profession,
      years,
      prizeLabel: prize,
      category: "nobel",
    });
  }
  return rows;
}

function buildPoolQuotes() {
  const quotes = [];
  let id = 1;

  for (const [category, items] of Object.entries(POOLS)) {
    for (const [quote, person, profession, years] of items) {
      quotes.push({
        id: id++,
        category,
        quote,
        person,
        profession,
        years,
        prizeLabel: "",
        displayName: `${person} ${profession}`,
      });
    }
  }

  const nobel = parseNobelFromMd();
  for (const item of nobel) {
    quotes.push({
      id: id++,
      category: "nobel",
      quote: item.quote,
      person: item.person,
      profession: item.profession,
      years: item.years,
      prizeLabel: item.prizeLabel,
      displayName: item.profession
        ? `${item.person} ${item.profession}`
        : item.person,
    });
  }

  return quotes;
}

